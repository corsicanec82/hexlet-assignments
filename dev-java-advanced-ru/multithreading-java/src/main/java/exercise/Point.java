package exercise;

// BEGIN
class Point {
    private int x;
    private int y;

    Point(int coordinateX, int coordinateY ) {
        this.x = coordinateY;
        this.y = coordinateX;
    }

    public int getX() {
        return this.x;
    }

    public int getY() {
        return this.y;
    }
}
// END
